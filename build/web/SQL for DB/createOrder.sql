CREATE TABLE ORDERS (
    ORDERID int NOT NULL PRIMARY KEY
        GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
    PRODUCT_ID int,
    NAME VARCHAR(50),
    DETAIL VARCHAR(100),
    QUANTITY int,
    PRICE double,
    TYPE VARCHAR(30),
    ORDDATE VARCHAR(15),
    ORDSTATUS VARCHAR(15),
    CONSTRAINT PROFUCTS FOREIGN KEY (PRODUCT_ID)
    REFERENCES PRODUCTS(PRODUCT_ID)
);
